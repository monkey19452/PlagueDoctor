								<h3>Dlaczego ludzie się nie myją?</h3>

								<p style="text-indent:40px;">Średniowieczny Kościół nie zachęcał do zbytniego dbania o siebie. Za św. Hieronimem powtarzano, że: „ten, kogo chrzest oczyścił,
                                     nie musi się kąpać raz drugi”. Średniowiecze nie było jednak mroczne, zacofane i brudne. Poziom higieny często był znacznie wyższy
                                     niż w wiekach późniejszych. Miniatura z rękopisu z lat 60.–70. XIV w. (British Library, Royal 6 E VI) Dobry chrześcijanin powinien
                                      być brudny na ciele i czysty na duszy, a przyziemne sprawy związane z własnym wyglądem nie powinny odciągać go od spraw boskich – 
                                      tak nauczał Kościół. We wczesnym średniowieczu władze kościelne wręcz zakazywały kąpieli w niedziele i święta. Pod koniec epoki niedomyty
                                       chrześcijanin stał w opozycji do wykąpanego niewiernego. Gdy w 1492 roku zdobyto Grenadę, wojska Ferdynanda i Izabeli zburzyły wszystkie 
                                       muzułmańskie łaźnie. W następnych latach inkwizycja jako dowód przeciwko Żydom i Maurom wyciągała argument, że zażywali ochoczo kąpieli.
                                        Hiszpańscy spowiednicy nie udzielali rozgrzeszenia kobietom, które kąpały się regularnie. Średniowieczne stronienie od kąpieli miało wymiar
                                         ascetyczny – ludzie poświęcali swoją cielesność, by choć minimalnie zbliżyć się do chrystusowego ideału. I tak np. św. Fintan z 
