                                <h3>Chirurg z horroru, czyli najlepszy lekarz średniowiecza.</h3>
								<p style="text-indent:40px;">  Henri de Mondeville jest współcześnie uznawany za najlepszego lekarza okresu średniowiecza. Zamiast, wzorem większości 
                                kolegów po fachu, unikać kontaktu z chorym i czekać na spełnienie woli boskiej, działał. I właśnie to najbardziej przerażało jego pacjentów. Cóż, nie bez powodu!
                                De Mondeville urodził się gdzieś na terenie Normandii i, zgodnie ze zwyczajem, nadał sobie nazwisko w oparciu o nazwę rodzinnej wioski. Mimo to, precyzyjnie miejsca 
                                w którym przyszedł na świat nie udało się jeszcze historykom określić, podobnie jak prawidłowej wymowy i pisowni imienia i nazwiska. Niektórzy są zdania, że lekarz
                                 mógł przyjść na świat w wiosce Mandeville nieopodal Caen, inni zaś twierdzą, że miało to miejsce w Emondeville. Następca de Mondeville’a, Guy de Chauliac, pisząc
                                  na jego temat stosuje zmienioną formę nazwiska „Hermondaville”, a w niektórych tekstach znajdują się kolejne wariacje: „Mondavilla”, „Amondavilla”, „Armandavilla”, 
                                  „Amoda villa” czy „Mundi villa”.
                                </p>
								<h3>Wino dobre na wszystko</h3>
                                <p style="text-indent:40px;"> Nic z resztą dziwnego. De Mondeville operował w czasach, kiedy nie było mowy o jakimkolwiek znieczuleniu. Przeprowadzał amputacje,
                                 tamował krwotoki i opatrywał rany sięgające organów wewnętrznych. Jego największym osiągnięciem, zdaniem współczesnych lekarzy, było odrzucenie przekonania, że ropienie
                                  jest kluczowym procesem w gojeniu. Podczas, gdy Theodoric był jeszcze zwolennikiem poszerzania ran i wywoływania ropienia celowo, de Mondeville opracował zupełnie odmienną 
                                  metodę. Nie dotykał rany bez wyraźnej potrzeby, a już na pewno nie doprowadzał do większego krwawienia, zamiast tego starając się je tamować po usunięciu obcych ciał.
                                   Następnie omywał uszkodzone tkanki winem (to tyczyło się również organów wewnętrznych) i zakładał opatrunki z materiału nasączonego – również winem. Starał się utrzymywać 
                                   ranę wilgotną, uważając, że kontakt z powietrzem jest najbardziej niebezpieczny dla pacjenta. Rany jelit polecał zszywać, a następnie również przemywać winem, osuszyć i oprószyć
                                    „proszkiem na bliznowacenie” złożonym z suszonego korzenia granatu oraz jego kwiatów, a także spróchniałego dębowego drewna (co mogło być, zupełnie przypadkiem, środkiem 
                                    zapobiegawczym przeciw zakażeniu, ponieważ niektóre grzyby rozkładające drewno wydzielają naturalne antybiotyki). Co ciekawe, po śmierci de Mondeville’a w 1320 roku jego metoda – 
                                    jednocześnie przerażająca i skuteczniejsza od poprzednio stosowanych – została porzucona przez bezpośredniego następcę, Guya de Chauliac. Tak oto przepadł początek chirurgii 
                                    antyseptycznej, zapomniany, niemal sześćset lat przed wynalezieniem współczesnych środków odkażających.
                                </p>
                               
