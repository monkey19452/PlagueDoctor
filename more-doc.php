								<h3>Lekarze w średniowieczu</h3>

								<p style="text-indent:40px;">Medycyna ówczesna znacznie różniła się od współczesnej. Lekarze wtedy i dziś w jednym byli podobni: na ogół świetnie
                                 zarabiali. Niewiele nadal wiemy o medycynie w wiekach średnich. Powód jest oczywisty: brak dostatecznej ilości źródeł.
                                Mnożą się za to stereotypy. Wynikają one przede wszystkim z braku wiedzy o tamtej epoce, szczególnie w aspekcie życia codziennego. Powszechnie
                                 wiadomo, że nasi przodkowie przed wiekami żyli krótko, w dużym stopniu dlatego, iż medycyna była w powijakach, a lekarze nieraz bardziej szkodzili,
                                  niż pomagali. Oczywiście, że i dzisiaj jest podobnie. Ponadto zarówno w średniowieczu, jak i obecnie lekarze stanowili elitę finansową. Swoją
                                   drogą, ciekawą rzeczą jest, że w Polsce średniowiecznej zdecydowanie najdłużej żyły mniszki. Przeciętna ich wieku, jak wynika z badań szczątków 
                                   kostnych, wynosiła 53 lata. Była ona wyższa o prawie 19 lat w porównaniu z przeciętnym trwaniem życia kobiet zamężnych, o 12 lat mężczyzn oraz o
                                    ponad 10 lat członków dynastii piastowskiej. Dlaczego mniszki żyły najdłużej? Bo nie rodziły. Śmiertelność okołoporodowa była wówczas ogromna. 
                                    Lekarze w średniowieczu rekrutowali się w sporej części ze stanu duchownego. Z czasem władze kościelne zakazywały im chirurgii, czyli leczenia
                                     ran, wychodząc z założenia, że kapłan nie powinien mieć styczności z nieczystą, jak wtedy uważano, krwią, a niedługo później z ciałem Chrystusa
                                      w postaci hostii podczas Eucharystii. Chirurgię, dziedzinę cieszącą się mniejszym uznaniem, uznawaną za czyste rzemiosło, przejęły zatem osoby 
                                      świeckie. Janko z Czarnkowa w sposób szczegółowy opisał przebieg choroby króla Kazimierza Wielkiego oraz próby jej leczenia podjęte przez 
                                      lekarzy. Król nie przeżył, lecz opis kronikarza, jedyny tego rodzaju w polskim piśmiennictwie średniowiecznym, zdaniem znawców dowodzi, że
                                       poziom medycyny w Polsce w drugiej połowie XIV stulecia był stosunkowo wysoki. Kazimierz Wielki spadł z konia 9 września 1370 r. w czasie 
                                       polowania. Kontuzja nogi spowodowała gorączkę. Dzięki zabiegom dwóch lekarzy królewskich: Henryka z Kolonii i Mateusza ze Stachowa, została
                                        zbita. I najpewniej król pożyłby dłużej, gdyby stosował się do zaleceń lekarzy. Zalecali monarsze, by nie jadł orzechów laskowych, surowych 
                                        owoców i ciężkostrawnych pokarmów, ale przede wszystkim, by nie korzystał z łaźni. Władca nie posłuchał i poszedł do niej, gdy przybył do
                                         Sandomierza. Dostał gorączki, która wzmogła się, gdy ruszył do Krakowa, po drodze pijąc zimną wodę. Henryk z Kolonii ponownie zniwelował
                                          gorączkę. Król był jednak uparty i kiedy poczuł się lepiej, kolejny raz zlekceważył zalecenia lekarza. Wysoką gorączkę i tym razem medycy 
                                          zbili, stosując środki przeczyszczające. W drodze do Krakowa choroba kolejny raz zaatakowała króla. Na Wawelu zajęło się nim, jak można
                                           wnosić z kroniki, kilkuosobowe grono medyków, ale już nie pomogli. Monarcha zmarł 5 listopada 1370 r. Po wiekach prof. Roman Grodecki,
                                            wybitny mediewista z UJ, przedstawił lekarzowi prof. Leonowi Trochowiczowi symptomy choroby opisane przez Janka z Czarnkowa, nie mówiąc, 
                                            o kogo chodzi. Współczesny uczony, lekarz, nie miał wątpliwości: powodem śmierci było zapalenie płuc.
