                                <h3>Mydłem, płynem do kąpieli, a nawet piwem</h3>
								<p style="text-indent:40px;">„Kąpiel czyści zewnętrzne członki ciała z brudu po ćwiczeniach” – czytamy w „Regimen Sanitatis Salernitanum”, wierszowanym zbiorze ogólnych
                                     zaleceń dotyczących diety i higieny. Dzieło powstało ok. XIII wieku, ale jego popularność sprawiła, że jeszcze w XIV stuleciu. dopisywano
                                      doń kolejne porady. Już to świadczy o wysokim poziomie zainteresowania sprawami higienicznymi. Kobiety w łaźni, rys. Albrechta Durera,
                                       koniec XV w. Lektura tego typu traktatów pokazuje ponadto, że średniowieczni higieniści i lekarze traktowali kąpiel jako czynność poważną
                                        i wymagającą uwagi. „Secreta secretorum”, jeden z ważniejszych traktatów medycznych epoki, miał nawet osobny rozdział poświęcony wyłącznie
                                         kąpaniu się. Inny traktat, „Magninius Mediolanesis” zawierał aż 57 zaleceń kąpielowych, różnych dla ludzi starszych, kobiet w ciąży i 
                                         podróżujących. W XI wieku powstał nawet poświęcony higienie kobiet traktat „De ornatu mulierum”. Napisała go niejaka Trotula z Salerno,
                                          która według tradycji uznawana jest za kobietę chirurga czerpiącą wiedzę z doświadczeń arabskich. W swoim dziele przedstawiała sposoby
                                           na oczyszczenie cery, czy wybielanie zębów. Zalecała m.in. stosowanie łupin orzechów oraz płukanie ust winem z odrobiną soli.
                                            W kąpieli doradzała wykorzystywanie nie tylko mydła, ale także kombinacji ziół i olejków do wody. Zazwyczaj wykorzystywano rumianek, 
                                            ślaz, koper włoski a nawet brzeczkę piwną – półprodukt filtracji zacieru do produkcji piwa.
                                </p>
								<h3>Wspólne mycie</h3>
                                <p style="text-indent:40px;">O ile bogacze i władcy mieli dostęp do własnych łaźni czy saun, większości mieszkańców nie było stać na prywatną łazienkę. Nie oznacza to,
                                     że byli skazani jedynie na kąpiel w rzekach i jeziorach. Po całym kontynencie rozsiane były łaźnie publiczne, gdzie każdy za odpowiednią opłatą 
                                     mógł zmyć brud z ciała. W XIII-wiecznym Paryżu działało 26 takich przybytków, ale już w XII wieku żyjący tam niejaki Alexander Neckham narzekał,
                                      że rano budziły go narzekania kapiących się ludzi z powodu rzekomo zbyt gorącej wody. Także w Polsce w samym tylko Krakowie w XIV wieku funkcjonowało 
                                      12 łaźni. Zresztą tego typu rozrywki na ziemiach słowiańskich nie były niczym nowym. Archeolodzy odkryli na terenach Rusi, Czech i Polski 
                                      pochodzące nawet z IX–X wieku łaźnie słowiańskie. O kąpielach naszych przodków wspominają choćby Ibrahim ibn Jakub oraz Gall Anonim.
                                    W miastach dojrzałego średniowiecza (XII–XV wiek) łaźnie stały się powszechnym sposobem na zachowanie czystości. Często budowano je w sąsiedztwie
                                     piekarni, by ciepło z pieców wykorzystać do podgrzania wody. Ich powszechność była na tyle duża, że nawet w mniejszych miastach można było 
                                     znaleźć tzw. domy kąpieli. W Southwark, mieścinie na drugim brzegu Tamizy niedaleko Londynu, znajdowało się nawet 18 tego typu przybytków.

